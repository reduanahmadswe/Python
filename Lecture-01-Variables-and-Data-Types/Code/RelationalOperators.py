

#relational operators
a = 10
b = 20

print(a == b)   #equal to | the answer is false
print(a > b)    #greater than | the answer is false
print(a < b)    #less than | the answer is true
print(a != b)   #not equal to | the answer is true
print(a >= b)   #greater than or equal to | the answer is false
print(a <= b)   #less than or equal to | the answer is true